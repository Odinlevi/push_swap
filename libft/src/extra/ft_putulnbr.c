#include "libft.h"

void	ft_putulnbr(unsigned long nb)
{
	if (nb >= 10)
	{
		ft_putulnbr(nb / 10);
		ft_putulnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
